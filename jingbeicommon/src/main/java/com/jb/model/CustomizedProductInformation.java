package com.jb.model;


public class CustomizedProductInformation {

  private String id;
  private String name;
  private Integer importMin;
  private Integer importMax;
  private Integer exitMin;
  private Integer exitMax;
  private Integer ratedFlow;
  private String regulatorKit;
  private String outerBox;
  private String boxMaterials;
  private String remarkSection;
  private String prId;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public Integer getImportMin() {
    return importMin;
  }

  public void setImportMin(Integer importMin) {
    this.importMin = importMin;
  }


  public Integer getImportMax() {
    return importMax;
  }

  public void setImportMax(Integer importMax) {
    this.importMax = importMax;
  }


  public Integer getExitMin() {
    return exitMin;
  }

  public void setExitMin(Integer exitMin) {
    this.exitMin = exitMin;
  }


  public Integer getExitMax() {
    return exitMax;
  }

  public void setExitMax(Integer exitMax) {
    this.exitMax = exitMax;
  }


  public Integer getRatedFlow() {
    return ratedFlow;
  }

  public void setRatedFlow(Integer ratedFlow) {
    this.ratedFlow = ratedFlow;
  }


  public String getRegulatorKit() {
    return regulatorKit;
  }

  public void setRegulatorKit(String regulatorKit) {
    this.regulatorKit = regulatorKit;
  }


  public String getOuterBox() {
    return outerBox;
  }

  public void setOuterBox(String outerBox) {
    this.outerBox = outerBox;
  }


  public String getBoxMaterials() {
    return boxMaterials;
  }

  public void setBoxMaterials(String boxMaterials) {
    this.boxMaterials = boxMaterials;
  }


  public String getRemarkSection() {
    return remarkSection;
  }

  public void setRemarkSection(String remarkSection) {
    this.remarkSection = remarkSection;
  }


  public String getPrId() {
    return prId;
  }

  public void setPrId(String prId) {
    this.prId = prId;
  }

}
